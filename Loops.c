#include <stdio.h>
#include <stdlib.h>

int main()
{
    int iCnt = 0;
    iCnt=1;
    for (iCnt=1 ;iCnt<=4 ; iCnt++)
    {
        printf("Jay Ganesh\n");
    }
   

    //While Loop
    iCnt=1;
    while( iCnt<=4)
    {
        printf("Jay Ganesh\n");
        iCnt++;  
    }

    //DO WHILE
    iCnt=1;
    do
    {
        printf("Jay Ganesh\n");
        iCnt++;
    } while (iCnt<=4);
    

    
    return 0;
}
